#include <iostream>

int main() {
    // Declare and initialize two numbers
    int num1 = 5;
    int num2 = 10;

    // Calculate the product
    int product = num1 * num2;

    // Display the result
    std::cout << "The product of " << num1 << " and " << num2 << " is: " << product << std::endl;

    return 0;
}

